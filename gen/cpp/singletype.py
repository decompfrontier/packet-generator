from typing import cast

from gen.generator import Generator
from schema import *
from datetime import datetime

class CppSingleType(Generator):
    """
    Generates base C++ type classes.
    """

    TYPE_MAPPING: dict[type, str] = {
        int:            "uint32_t",
        intstr:         "uint32_t",
        long:           "uint64_t",
        longstr:        "uint64_t",
        str:            "std::string",
        bool:           "bool",
        intbool:        "bool",
        strbool:        "bool",
        datetimeunix:   "pkghlp::chronotime",
        datetime:       "pkghlp::chronotime",
        floatstr:       "pkghlp::float32", # support for c++20 floating point
        float:          "pkghlp::float32", # support for c++20 floating point
        double:         "pkghlp::float64", # support for c++20 floating point
        list:           "std::vector",
        dict:           "std::unordered_map",
        commalist:      "std::vector",
        atlist:         "std::vector"
    }
    """Mapping of Python python to C++ strings."""

    def get_start_mark(self, file_name: str) -> str:
        return """

        /*
            This file was automatically generated by DecompFrontier C++ packet generator.

            DO NOT MODIFY THIS FILE! ANY CHANGE TO THIS FILE WILL BE DISCARDED IN THE NEXT
            GENERATOR RUN.
            INSTEAD, MODIFY THE FILE "%s" INSIDE THE PACKET GENERATOR REPOSITORY AND
            RUN THE GENERATOR AGAIN!
        */
        #pragma once
        #include <pkgen_cpp_helpers.hpp> /* C++ helpers */

        """ % file_name

    def add_import(self, imp: str, output_dir: str) -> str:
        new_imp = imp.replace(".", "_")
        return "#include <%s>" % new_imp

    def get_end_mark(self) -> str:
        return ""

    def get_extension(self) -> str:
        return ".hpp"

    def _step_primitive_type(self, type_: type) -> str:
        # primitive type conversion
        if type_ in self.TYPE_MAPPING:
            return self.TYPE_MAPPING[type_]
        else: # if itn's not a Python builtin type, then it's a custom type like a custom struct
            return type_.__name__

    def _step_struct_type(self, type_: typetuple) -> str:
        cpp_type = self._step_primitive_type(type_[0])
        args = type_[1]
        cpp_template = ""

        # if the types has arguments then it's a template type...
        if args is not None and len(args) > 0:
            for type_arg in args:
                # generate the C++ type for the specified subtype,
                # this piece is required to have something like list[str]
                if type(type_arg) == tuple:
                    sub_type = self._step_struct_type(type_arg)
                else:
                    sub_type = self._step_primitive_type(type_arg)

                cpp_template += "%s," % sub_type
            cpp_template = "<%s>" % (cpp_template[:-1])

        return "%s%s" % (cpp_type, cpp_template)

    def _step_struct_field(self, current: GeneratorField) -> str:
        # generate a single struct field
        field_type = self._step_struct_type(current.type_id) # grab the type
        if current.default_action == DefaultType.Omit:
            # any field that is omittable is a std::optional<>
            field_type = "std::optional<%s>" % field_type

        return """
        /**
            %s
        */
        %s %s;
        """ % (current.documentation, field_type, current.name)

    def _step_struct(self, struct: GeneratorStruct) -> str:
        fields_to_buffer = ""
        # generate all fields of a struct
        for it in struct.fields:
            fields_to_buffer += self._step_struct_field(it)

        match struct.array_step:
            # No array or single are the same in C++ types, glaze will take
            ##  care of the rest
            case ArrayStep.NoArray | ArrayStep.Single:
                return """
        /**
            %s
        */
        struct %s {
            %s
        };
        """ % (struct.documentation,
               struct.name,
               fields_to_buffer)
            case ArrayStep.Array:
                # In case of an array we generate:
                ## an actual array type
                ## a base array element that contains the struct declaration
                return """
        /**
            Single element of %s.
        */
        struct %s_element {
            %s
        };

        /**
            %s
        */
        struct %s {
            std::deque<%s_element> data;
        };
        """ % (struct.name,
               struct.name,
               fields_to_buffer,
               struct.documentation,
               struct.name,
               struct.name)
            case _:
                raise Exception("Unknown array step")

    @staticmethod
    def _step_enum_field(current: GeneratorField) -> str:
        is_flag = issubclass(type(current.type_id), Flag)
        enum_val = cast(type[Enum], cast(object, current.type_id))

        if is_flag:
            cpp_val = "(1 << %s)" % enum_val.value
        else:
            cpp_val = enum_val.value

        return """
            /**
                %s
            */
            %s = %s,
        """ % (current.documentation,
               current.name,
               cpp_val)

    def _step_enum(self, struct: GeneratorStruct) -> str:
        fields_to_buffer = ""
        # generate all fields of an enum
        for it in struct.fields:
            fields_to_buffer += self._step_enum_field(it)

        return """
        /**
            %s
        */
        enum class %s {
            %s
        };
        """ % (struct.documentation,
               struct.name,
               fields_to_buffer)

    @staticmethod
    def _step_enum_string_field(current: GeneratorField) -> str:
        enum_val = cast(type[Enum], cast(object, current.type_id))

        return """
            /**
                %s
            */
            constexpr const auto %s = "%s";
        """ % (current.documentation,
               current.name,
               enum_val.value)

    def _step_enum_string(self, struct: GeneratorStruct) -> str:
        # because C++ does not have enum string, the idea
        # is that we generate the structure as a namespace
        # (this allows us to avoid conflicts with the fields name)
        # and generate a special ""structure type"" that maps to std::string
        # so this mimics the generation of a real class type.

        fields_to_buffer = ""
        # generate all fields of an enum
        for it in struct.fields:
            fields_to_buffer += self._step_enum_string_field(it)

        return """
        /**
            Namespace that contains the string values of %s.
        */
        namespace v_%s {
            %s
        };
        
        /**
            %s
        */
        using %s = std::string;
        """ % (struct.name,
               struct.name,
               fields_to_buffer,
               struct.documentation,
               struct.name)

    def step(self, struct: GeneratorStruct) -> str:
        if struct.class_type == ClassType.Struct:
            # generate a struct
            return self._step_struct(struct)
        elif struct.class_type == ClassType.Enumerator:
            return self._step_enum(struct)
        else:
            return self._step_enum_string(struct)