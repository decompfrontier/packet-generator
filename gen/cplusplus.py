from datetime import datetime
from schema import *
from .generator import Generator
from typing import cast

class CppGenerator(Generator):
    """A generic C++ generator DecompFrontier packets.

    This generator will read all the packet definitions and output their
    corresponding C++ declarations.
    """



    def get_start_mark(self, file_name: str) -> str:
        return """

/*
    This file was automatically generated by DecompFrontier C++ packet generator.
    
    DO NOT MODIFY THIS FILE! ANY CHANGE TO THIS FILE WILL BE DISCARDED IN THE NEXT
    GENERATOR RUN.
    INSTEAD, MODIFY THE FILE \"{}\" INSIDE THE PACKET GENERATOR REPOSITORY AND
    RUN THE GENERATOR AGAIN!
*/
#pragma once
#include <pkgen_cpp_helpers.hpp> /* C++ helpers */

""".format(file_name)

    def add_import(self, imp: str, _: str) -> str:
        return """#include <{}.h>
""".format(imp.replace(".", "_"))

    def get_end_mark(self) -> str:
        return """
/* EOF */"""

    def get_extension(self) -> str:
        return ".hpp"

    @staticmethod
    def _get_type_definition(class_type: ClassType) -> str:
        """
        Gets the C++ name declaration of the class type.

        :param class_type: Class type
        :return: C++ class type
        """
        match class_type:
            case ClassType.Struct:
                return "struct"
            case ClassType.Enumerator:
                return "enum class"
            case ClassType.EnumeratorString:
                return "namespace"
            case _:
                raise Exception("Class type not supported: {}".format(str(class_type)))

    def _get_field_type_definition(self, current_field: type) -> str:
        if current_field in self.TYPE_MAPPING:  # the current type is from a mapping
            cpp_type = self.TYPE_MAPPING[current_field]
        else:  # if it's not a python builtin type, threat this as a custom type
            cpp_type = current_field.__name__

        if current_field.type_arguments is not None:
            cpp_type = "".join((cpp_type, "<"))
            for type_argument in current_field.type_arguments:
                # get the cpp type for the subtypes...
                sub_cpp_type = self._get_field_type_definition(type_argument)
                pos = sub_cpp_type.find("\t")
                if pos != -1:
                    sub_cpp_type = sub_cpp_type[:pos]
                cpp_type = "".join((cpp_type, sub_cpp_type, ","))
            cpp_type = "".join((cpp_type[:-1], ">"))

        if current_field.default_action == DefaultType.Omit:
            cpp_type = "std::optional<{}>".format(
                cpp_type)  # append the optional<> to allow glaze to skip types that should be omitted if null

        return cpp_type

    def _get_field_definition(self, current_field: GeneratorField, parent: GeneratorStruct) -> str:
        """Generates the C++ type of the field.

        :param current_field: Generator field to get the C++ type from
        :param parent: Parent generator field
        :return: C++ type in string
        """
        if parent.class_type == ClassType.Enumerator or parent.class_type == ClassType.EnumeratorString:
            is_flag = issubclass(type(current_field.type_id), Flag)
            enum_val = cast(type[Enum], current_field.type_id)

            if parent.class_type == ClassType.Enumerator:
                start = ""
                center = " = "
                end = ","

                if is_flag:
                    value = "1 << {}".format(enum_val.value)
                else:
                    value = "{}".format(enum_val.value)
            else:
                start = "constexpr const auto ".format(current_field.documentation)
                center = " = \""
                end = "\";"
                value = "{}".format(enum_val.value) # C++ doesn't have actual enum string, so an enum flag string does not make sense!

            return """/*! {} */
    {}{}{}{}{}""".format(current_field.documentation, start, current_field.name, center, value, end)
        else:
            if current_field.type_id in self.TYPE_MAPPING: # the current type is from a mapping
                cpp_type = self.TYPE_MAPPING[current_field.type_id]
            else: # if it's not a python builtin type, threat this as a custom type
                cpp_type = current_field.type_id.__name__

            if current_field.type_arguments is not None:
                cpp_type = "".join((cpp_type, "<"))
                for type_argument in current_field.type_arguments:
                    # get the cpp type for the subtypes...
                    sub_cpp_type = self._get_field_definition(GeneratorField(type_id = type_argument), parent)
                    pos = sub_cpp_type.find("\t")
                    if pos != -1:
                        sub_cpp_type = sub_cpp_type[:pos]
                    cpp_type = "".join((cpp_type, sub_cpp_type, ","))
                cpp_type = "".join((cpp_type[:-1], ">"))

            if current_field.default_action == DefaultType.Omit:
                cpp_type = "std::optional<{}>".format(
                    cpp_type)  # append the optional<> to allow glaze to skip types that should be omitted if null

            return """/*! {} */
    {} {}{{}};""".format(current_field.documentation, cpp_type, current_field.name)

    def step(self, struct: GeneratorStruct) -> str:
        """Generates the C++ class definition.

        :param struct: Structure to generate
        :return: Generated C++ class
        """

        # 1. struct start
        if struct.class_type == ClassType.EnumeratorString:
            # because C++ does not have enum string, the idea
            # is that we generate the structure as a namespace
            # (this allows us to avoid conflicts with the fields name)
            # and generate a special ""structure type"" that maps to std::string
            # so this mimics the generation of a real class type.
            output = """/*! {} */
using {} = std::string;

namespace v_{} {{
""".format(struct.documentation, struct.name, struct.name)
        else:
            struct_name = struct.name
            if struct.array_step != ArrayStep.NoArray:
                struct_name = "".join((struct_name, "Data"))

            output = """/*! {} */
{} {} {{
""".format(struct.documentation, self._get_type_definition(struct.class_type), struct_name)

        # 2. struct members
        for struct_field in struct.fields:
            field_output = self._get_field_definition(struct_field, struct)
            output = "".join((output, "\t", field_output, "\n"))
        # 3. struct end
        output = "".join((output, "\n};\n"))

        match struct.array_step:
            case ArrayStep.Array:
                output = "".join((output, """/*! {} */
struct {} {{
    /*! Type definition of the array data */
    using TYPE = {}Data;
    /*! Array that contains the actual content of the object */
    std::deque<TYPE> data;
}};\n""".format(struct.documentation, struct.name, struct.name) ))
            #case ArrayStep.Single:
            #    output = "".join((output, """struct {} {{
#    using TYPE = {}Data;
#    using ARRAY_TYPE = std::array<TYPE, 1>;
#
#    ARRAY_TYPE data;
#}};\n""".format(struct.name, struct.name) ))
            case _:
                pass
        return output
